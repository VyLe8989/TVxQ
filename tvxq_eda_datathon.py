# Net revenue (quan trọng hơn)
df["net_revenue"] = df["gross_revenue"] - df["discount_amount"].fillna(0)

discount_analysis = df.groupby("category").agg(
    gross_revenue=("gross_revenue", "sum"),
    discount_amount=("discount_amount", "sum"),
    net_revenue=("net_revenue", "sum")
)

discount_analysis["discount_rate"] = (
    discount_analysis["discount_amount"] / discount_analysis["gross_revenue"]
)

print(discount_analysis)

"""Tỷ suất lợi nhuận của các danh mục sản phẩm"""

# cost
df["cost"] = df["quantity"] * df["cogs"].fillna(0)

# profit dùng net revenue (chuẩn hơn)
df["profit"] = df["net_revenue"] - df["cost"]

margin_analysis = df.groupby("category").agg(
    net_revenue=("net_revenue", "sum"),
    profit=("profit", "sum")
)

margin_analysis["margin_pct"] = (
    margin_analysis["profit"] / margin_analysis["net_revenue"]
)

print(margin_analysis)

