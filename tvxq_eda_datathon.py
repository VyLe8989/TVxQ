# ===================== IMPORT =====================
import pandas as pd
import matplotlib.pyplot as plt

# ===================== ĐỌC DỮ LIỆU =====================
orders = pd.read_csv("Data/orders.csv")
web = pd.read_csv("Data/web_traffic.csv")

# ===================== XỬ LÝ DATETIME =====================
orders["order_date"] = pd.to_datetime(orders["order_date"])
web["date"] = pd.to_datetime(web["date"])

# ===================== FILTER ĐƠN HỢP LỆ =====================
orders_valid = orders[orders["order_status"] != "cancelled"]

# =========================================================
# 1. CR THEO THỜI GIAN (CONFIRM VẤN ĐỀ)
# =========================================================
orders_m = orders_valid.set_index("order_date").resample("ME")["order_id"].nunique()
sessions_m = web.set_index("date").resample("ME")["sessions"].sum()

cr = (orders_m / sessions_m).dropna()

plt.figure(figsize=(8,4))
plt.plot(cr)
plt.title("Conversion Rate Trend Over Time")
plt.xlabel("Time")
plt.ylabel("CR")
plt.show()

# =========================================================
# 2. CR THEO DEVICE (AI KÉO XUỐNG?)
# =========================================================
orders_device = orders_valid.groupby("device_type")["order_id"].count().reset_index()

# Không có sessions theo device → không tính CR chuẩn được
# → dùng tỷ trọng đơn hàng thay thế
total_orders = orders_device["order_id"].sum()
orders_device["share"] = orders_device["order_id"] / total_orders

print("\nOrders share by Device (proxy for CR):")
print(orders_device)

# =========================================================
# 3. CR THEO CHANNEL (TRAFFIC CÓ CHẤT LƯỢNG KHÔNG?)
# =========================================================
orders_channel = orders_valid.groupby("order_source")["order_id"].count().reset_index()
sessions_channel = web.groupby("traffic_source")["sessions"].sum().reset_index()

cr_channel = orders_channel.merge(
    sessions_channel,
    left_on="order_source",
    right_on="traffic_source"
)

cr_channel["cr"] = cr_channel["order_id"] / cr_channel["sessions"]

print("\nCR by Channel:")
print(cr_channel[["order_source", "cr"]])

# =========================================================
# 4. TRAFFIC vs ORDERS (CHECK QUALITY)
# =========================================================
plt.figure(figsize=(8,4))
plt.plot(sessions_m, label="Sessions")
plt.plot(orders_m, label="Orders (valid)")
plt.legend()
plt.title("Sessions vs Orders Over Time")
plt.xlabel("Time")
plt.ylabel("Count")
plt.show()

# =========================================================
# 5. TRAFFIC QUALITY THEO CHANNEL
# =========================================================
bounce_by_channel = web.groupby("traffic_source").apply(
    lambda x: (x["bounce_rate"] * x["sessions"]).sum() / x["sessions"].sum()
).reset_index(name="weighted_bounce")

print("\nBounce Rate by Channel (weighted):")
print(bounce_by_channel)

duration_by_channel = web.groupby("traffic_source")["avg_session_duration_sec"].mean().reset_index()
print("\nSession Duration by Channel:")
print(duration_by_channel)
# ===================== VẼ BIỂU ĐỒ =====================

fig, axes = plt.subplots(1, 2, figsize=(12, 5))

# --- Biểu đồ 1: Bounce rate ---
axes[0].bar(bounce_by_channel["traffic_source"], bounce_by_channel["weighted_bounce"])
axes[0].set_title("Bounce Rate theo Channel")
axes[0].set_xlabel("Traffic Source")
axes[0].set_ylabel("Bounce Rate")
axes[0].tick_params(axis='x', rotation=45)

# --- Biểu đồ 2: Session duration ---
axes[1].bar(duration_by_channel["traffic_source"], duration_by_channel["avg_session_duration_sec"])
axes[1].set_title("Session Duration theo Channel")
axes[1].set_xlabel("Traffic Source")
axes[1].set_ylabel("Avg Duration (sec)")
axes[1].tick_params(axis='x', rotation=45)

plt.tight_layout()
plt.show()

